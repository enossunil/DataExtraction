package com.india.elects.ls.year2014.exception;

public class UnexpectedExcelFormat extends RuntimeException {

	public UnexpectedExcelFormat() {
	}

	public UnexpectedExcelFormat(String arg0) {
		super(arg0);
	}

	public UnexpectedExcelFormat(Throwable arg0) {
		super(arg0);
	}

	public UnexpectedExcelFormat(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public UnexpectedExcelFormat(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
